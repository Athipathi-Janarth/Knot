    private void ProfileImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProfileImgActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg", "gif", "png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            photoPath = path;
            profileImg.setIcon(ResizeImage(path));
        } else if (result == JFileChooser.CANCEL_OPTION) {
            System.out.println("No File Select");
        }
    }//GEN-LAST:event_ProfileImgActionPerformed
    public ImageIcon ResizeImage(String ImagePath) {
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(125, 145, Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }
    public boolean validateCoupleUser(){
        boolean valid = true;
        if(txtyourName.getText().isEmpty() || txtyourName1.getText().isEmpty()){
            valid=false;
        }
        if(txtyourZodiac.getText().isEmpty() || txtyourZodiac1.getText().isEmpty()){
            valid=false;
        }
        if(txtUserName.getText().isEmpty() || txtPassword.getText().isEmpty()){
            valid=false;
        }
        if(txtEmail.getText().length()<2 || !txtEmail.getText().matches("^(.+)@(\\S+)$")){
            valid=false;
        }
        if(weddingDateChooser.getDate()== null){
        valid= false;
        }
        return valid;
    }
    public boolean validateSingleUser(){
        boolean valid = true;
        boolean radioSelected;
        boolean radioSelected1;
        try {
            radioSelected = genderBtn.getSelection().isSelected();
        } catch (Exception e) {
            radioSelected = false;
        }
        if (!radioSelected) {
            valid = false;
        }
        if(txtName.getText().isEmpty() || txtZodiac.getText().isEmpty()){
            valid=false;
        }
        if(txtUserName1.getText().isEmpty() || txtPassword1.getText().isEmpty()){
            valid=false;
        }
       if (!isInteger(txtAge.getText())) {
            valid = false;
        }
       if (!isInteger(txtAge1.getText())) {
            valid = false;
        }
       try {
            radioSelected1 = genderBtn1.getSelection().isSelected();
        } catch (Exception e) {
            radioSelected1 = false;
        }
        if (!radioSelected1) {
            valid = false;
        }
        if(txtpreferedZodiac.getText().isEmpty()){
            valid=false;
        }
        return valid;
    }
    private boolean isInteger(String txtField) {
        if (txtField.matches("\\d+")) {
            return true;
        } else {
            return false;
        }
    }
