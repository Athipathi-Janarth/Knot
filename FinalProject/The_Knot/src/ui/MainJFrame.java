    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        mainPanelSysadmin syspanel= new mainPanelSysadmin(system);
        
        
        String username=txtuserName.getText();
        String password=txtPwd.getText();
        if(username.equals("sysadmin")&& password.equals("sysadmin")){
            mainPanel.add("workArea",syspanel);
            loginPanel.setVisible(false);
            upperPanel.setVisible(true);
            mainPanel.setVisible(true);
            clearLogin();
        }
        else{
        userAccount = system.getCoupledirectory().authenticateUser(username, password);
        if(userAccount!=null){
            loginPanel.setVisible(false);
            upperPanel.setVisible(true);
            mainPanel.setVisible(true);
            userpanel = new mainPanelUser(userAccount);
            mainPanel.add("workArea",userpanel);
            clearLogin();
           
        }
        else{
            employeeAccount =system.getEmployeedirectory().authenticateUser(username, password);
             if(employeeAccount!=null){
                loginPanel.setVisible(false);
                upperPanel.setVisible(true);
                mainPanel.setVisible(true);
                mainPanel.add("workArea",employeeAccount.getRole().createWorkArea(system,employeeAccount));
                
                clearLogin();
             }else{
            singleuserAccount = system.getSingleUserlist().authenticateUser(username, password);
            if(singleuserAccount!=null){
            loginPanel.setVisible(false);
            upperPanel.setVisible(true);
            mainPanel.setVisible(true);
            singleuserpanel = new mainPanelSingleUser(system,singleuserAccount);
            mainPanel.add("workArea",singleuserpanel);
            clearLogin();
           
             }
             else{
             JOptionPane.showMessageDialog(null, "No User Found");
             }
             }
        }
        }
        
       
        //syspanel.setSize(1310, 630);
                   
            CardLayout layout = (CardLayout) mainPanel.getLayout();
            layout.next(mainPanel);
    }//GEN-LAST:event_btnLoginActionPerformed

